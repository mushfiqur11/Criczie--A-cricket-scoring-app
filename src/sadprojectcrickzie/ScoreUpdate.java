    public void disableWickets(int run,int legal){
        if(run != 0 || legal != 0){
            this.UpdateWicketNotOut.setEnabled(true);
            CurrentWicket = "NotOut";
            this.UpdateWicketNotOut.setSelected(true);
            this.UpdateWicketBowled.setSelected(false);
            this.UpdateWicketBowled.setEnabled(false);
            this.UpdateWicketCatch.setSelected(false);
            this.UpdateWicketCatch.setEnabled(false);
            this.UpdateWicketLBW.setSelected(false);
            this.UpdateWicketLBW.setEnabled(false);
            this.UpdateWicketStumped.setSelected(false);
            this.UpdateWicketStumped.setEnabled(false);
            this.UpdateWicketRunOut.setEnabled(true);
        }
        if(run >=4){
            this.UpdateWicketRunOut.setEnabled(false);
        }
        if(run == 0 && legal == 0){
            this.UpdateWicketBowled.setEnabled(true);
            //UpdateWicketNotOut.setSelected(true);
            //UpdateWicketBowled.setSelected(false);
            this.UpdateWicketBowled.setEnabled(true);
            //UpdateWicketCatch.setSelected(false);
            this.UpdateWicketCatch.setEnabled(true);
            //UpdateWicketLBW.setSelected(false);
            this.UpdateWicketLBW.setEnabled(true);
            //UpdateWicketStumped.setSelected(false);
            this.UpdateWicketStumped.setEnabled(true);
            //UpdateWicketRunOut.setSelected(true);
            this.UpdateWicketRunOut.setEnabled(true);
        }
        CurrentRun = run;
        CurrentLegal= legal;
    }
