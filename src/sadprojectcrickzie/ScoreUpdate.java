    private void PlayerSwap(){
        int temp=CurrentBatsmanSL1;
        CurrentBatsmanSL1=CurrentBatsmanSL2;
        CurrentBatsmanSL2=temp;
    }
    private void NextBatsman(){
        if(CurrentBatsmanSL1>CurrentBatsmanSL2){
            CurrentBatsmanSL1=CurrentBatsmanSL1+1;
        }
        else
            CurrentBatsmanSL1=CurrentBatsmanSL2+1;
    }
    private void BallChange(){
        CurrentBall+=1;
        if(CurrentBall>=6){
            NewOver();
        }
        
    }
    private void NewOver(){
        CurrentBall=0;
        CurrentOver+=1;
    }
